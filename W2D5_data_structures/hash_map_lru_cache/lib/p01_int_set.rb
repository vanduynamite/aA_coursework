class IntSet
  def initialize(num_buckets = 20)
    @store = Array.new(num_buckets) { Array.new }
    @size = num_buckets
  end

  def insert(num)
    @store[num % @size] << num unless @store.include?(num)
  end

  def remove(num)
    @store[num % @size].delete(num)
  end

  def include?(num)
